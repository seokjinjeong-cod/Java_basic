package polymorphism;

import java.util.ArrayList;

public class CustomerTest {

	public static void main(String[] args) {

		ArrayList<Customer> customerList = new ArrayList<Customer>();
		
		Customer customerLee = new Customer(1010, "이순신");
		Customer customerShin = new Customer(1020, "신사임당");
		Customer customerHong = new GoldCustomer(1030, "홍길동");
		Customer customerYul = new GoldCustomer(1040, "이율곡");
		Customer customerKim = new VIPCustomer(1050, "김유신", 12345);
		
		customerList.add(customerLee);
		customerList.add(customerShin);
		customerList.add(customerHong);
		customerList.add(customerYul);
		customerList.add(customerKim);
		
		System.out.println("============고객 정보 출력============");
		
		for(Customer customer : customerList) {
			System.out.println(customer.showCustomerInfo());
		}
		
		System.out.println("============할인율과 보너스 포인트 계산============");
		
		for(Customer customer : customerList) {
			int price = 10000;
			System.out.println(customer.customerName + "님이 " + customer.calcPrice(price) + "원 지불하셨습니다.");
			System.out.println(customer.customerName + "님의 현재 보너스 포인트는 " + customer.bonusPoint + "점 입니다");
			
		}
	}
}
